/**
 *  @brief
 *      Gets a column from a character bit pattern. Lowest bit is on the top.
 *
 *  @param[in]
 *      ch  char to write
 *  @param[in]
 *      column  from left to right
 *  @param[in]
 *      scan  from top to bottom
 *  @return
 *      bit pattern for the char
 */
int FONT10X24B_getScanColumn(int ch, int column, int scan) {
	return font_DejaVuSansMonoBold_10w24h[ch][3*column+scan];
}


/**
 *  @brief
 *      Gets a column from a character bit pattern. Lowest bit is on the top.
 *
 *  @param[in]
 *      ch  char to write
 *  @param[in]
 *      column  from left to right
 *  @return
 *      bit pattern for the char
 */
int FONT10X24B_getColumn(int ch, int column) {
	return FONT10X24B_getScanColumn(ch, column, 0) + (FONT10X24B_getScanColumn(ch, column, 1) << 8) + (FONT10X24B_getScanColumn(ch, column, 2) << 16);
}

