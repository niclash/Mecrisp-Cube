/**
 *  @brief
 *      Gets a column from a character bit pattern. Lowest bit is on the top.
 *
 *  @param[in]
 *      ch  char to write
 *  @param[in]
 *      column  from left to right
 *  @param[in]
 *      scan  from top to bottom
 *  @return
 *      bit pattern for the char
 */
int FONT9X16B_getScanColumn(int ch, int column, int scan) {
	return font_DejaVuSansMonoBold_9w16h[ch][2*column+scan];
}


/**
 *  @brief
 *      Gets a column from a character bit pattern. Lowest bit is on the top.
 *
 *  @param[in]
 *      ch  char to write
 *  @param[in]
 *      column  from left to right
 *  @return
 *      bit pattern for the char
 */
int FONT9X16B_getColumn(int ch, int column) {
	return FONT9X16B_getScanColumn(ch, column, 0) + (FONT9X16B_getScanColumn(ch, column, 1) << 8);
}

